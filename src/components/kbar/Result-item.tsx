const ResultItem = forwardRef(
  (
    {
      action,
      active,
      currentRootActionId,
    }: {
      action: ActionImpl;
      active: boolean;
      currentRootActionId: ActionId;
    },
    ref: Ref<HTMLDivElement>
  ) => {
    const ancestors = useMemo(() => {
      if (!currentRootActionId) return action.ancestors;
      const index = action.ancestors.findIndex(
        (ancestor) => ancestor.id === currentRootActionId
      );
      return action.ancestors.slice(index + 1);
    }, [action.ancestors, currentRootActionId]);

    const getActionIcon = () => {
      if (!action.icon) {
        switch (action.id) {
          case "composeAction":
            return <ComposeIcon />;
          case "searchAction":
            return <SearchIcon />;
          case "contactsAction":
            return <ContactsIcon />;
          case "scheduleAction":
            return <ScheduleIcon />;
          case "aiResponseAction":
            return <AIResponseIcon />;
          case "settingsAction":
            return <SettingsIcon />;
          case "layoutAction":
            return <LayoutIcon />;
          case "analyticsAction":
            return <AnalyticsIcon />;
          default:
            return null;
        }
      }
      return action.icon;
    };
    ResultItem.displayName = "ResultItem"
    return (
      <div
        ref={ref}
        className={`px-3 py-2.5 flex items-center cursor-pointer relative z-10 ${active ? 'bg-gray-800/40' : 'bg-transparent'}`}
      >
        {getActionIcon() && (
          <div className="flex items-center justify-center w-8 h-8 mr-3 text-gray-400">
            {getActionIcon()}
          </div>
        )}
        <div className="flex flex-col flex-1">
          <div className={`text-sm ${active ? "text-gray-100" : "text-gray-300"}`}>
            {ancestors.length > 0 &&
              ancestors.map((ancestor) => (
                <Fragment key={`${action.id}-ancestor-${ancestor.id}`}>
                  <span className="opacity-50 mr-2">{ancestor.name}</span>
                  <span className="mr-2">&rsaquo;</span>
                </Fragment>
              ))}
            <span>{action.name}</span>
          </div>
        </div>
        {action.shortcut?.length ? (
          <div className="grid grid-flow-col gap-1">
            {action.shortcut.map((sc, index) => (
              <kbd
                key={`${action.id}-shortcut-${index}`}
                className="px-1.5 py-0.5 bg-gray-800 rounded text-xs text-gray-400"
              >
                {sc}
              </kbd>
            ))}
          </div>
        ) : null}
      </div>
    );
  }
);

export default ResultItem;